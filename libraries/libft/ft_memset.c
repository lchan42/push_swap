#include <stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*tab;

	tab = b;
	i = 0;
	while (i != len)
	{
		*(tab + i) = c;
		i++;
	}
	return (b);
}
